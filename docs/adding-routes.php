<h1>Adding routes</h1>

<h3>Adding routes</h3>

<p>Typing them in.</p>

<pre class="prettyprint linenums:0">
$router->get('authors', 'AuthorController@index');

$router->get('authors/{author}', 'AuthorController@show');
</pre>

<h3>Loading from a directory</h3>

<p>Type them in or to use Laravel style</p>

<pre class="prettyprint linenums:0">
use GeordieJackson\Router\Classes\Route;

Route::get('tags', 'TagController@index');

Route::get('tags/{tag}', 'TagController@show');
</pre>

<p>Create your routing directory i.e. path/to/routing/directory, then place as many files as you wish in it. e.g.</p>
<ul>
    <li>api.php</li>
    <li>web.php</li>
</ul>

<p>The router will scan them all and load the routes automatically.
    <br><br>
    Each file should contain:</p>

<pre class="prettyprint linenums:0">use GeordieJackson\Router\Classes\Route;</pre>

<p>and your route definitions. e.g.</p>

<pre class="prettyprint linenums:0">
Route::get('authors', 'AuthorController@index');
Route::get('authors/{author}', 'AuthorController@show');

Route::group(['prefix' => 'dashboard', 'name' => 'admin.'], function () {
    Route::get('/', 'DashboardController@index');
    Route::resource('users', 'UserController')->middleware('isAdmin');
    Route::resource('posts', 'PostController')->middleware('isModerator');
});
</pre>